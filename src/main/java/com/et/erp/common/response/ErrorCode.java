package com.et.erp.common.response;

import lombok.Getter;
import lombok.RequiredArgsConstructor;

/**
 * ERP 시스템 에러 코드 정의
 *
 * 모든 비즈니스 예외는 이 Enum의 코드와 메시지를 사용합니다.
 * ErpException 생성 시 이 Enum을 전달합니다.
 *
 * 코드 체계:
 *   E1xx - 공통 오류
 *   E2xx - 시스템 관리 오류
 *   E3xx - 인사관리 오류
 *   E4xx - 재무/회계 오류
 *   E5xx - 구매/재고 오류
 *   E6xx - 영업 오류
 */
@Getter
@RequiredArgsConstructor
public enum ErrorCode {

    // =============================================
    // E1xx: 공통 오류
    // =============================================

    /** 요청한 리소스를 찾을 수 없음 (HTTP 404) */
    NOT_FOUND("E100", "요청한 데이터를 찾을 수 없습니다."),

    /** 입력값 오류 (HTTP 400) */
    INVALID_INPUT("E101", "입력값이 올바르지 않습니다."),

    /** 중복 데이터 오류 (HTTP 409) */
    DUPLICATE_DATA("E102", "이미 존재하는 데이터입니다."),

    /** 권한 없음 (HTTP 403) */
    ACCESS_DENIED("E103", "접근 권한이 없습니다."),

    /** 인증 실패 (HTTP 401) */
    UNAUTHORIZED("E104", "인증이 필요합니다."),

    /** 서버 내부 오류 (HTTP 500) */
    INTERNAL_ERROR("E199", "서버 내부 오류가 발생했습니다."),

    // =============================================
    // E2xx: 시스템 관리 오류
    // =============================================

    /** 사용자 없음 */
    USER_NOT_FOUND("E200", "사용자를 찾을 수 없습니다."),

    /** 중복 로그인 ID */
    DUPLICATE_LOGIN_ID("E201", "이미 사용 중인 아이디입니다."),

    /** 비밀번호 불일치 */
    WRONG_PASSWORD("E202", "비밀번호가 올바르지 않습니다."),

    /** 비활성 사용자 */
    INACTIVE_USER("E203", "비활성화된 계정입니다. 관리자에게 문의하세요."),

    /** JWT 토큰 만료 */
    TOKEN_EXPIRED("E204", "로그인이 만료되었습니다. 다시 로그인해 주세요."),

    /** JWT 토큰 유효하지 않음 */
    TOKEN_INVALID("E205", "유효하지 않은 인증 토큰입니다."),

    // =============================================
    // E3xx: 인사관리 오류
    // =============================================

    /** 직원 없음 */
    EMPLOYEE_NOT_FOUND("E300", "직원을 찾을 수 없습니다."),

    /** 중복 사번 */
    DUPLICATE_EMP_NO("E301", "이미 존재하는 사번입니다."),

    /** 부서 없음 */
    DEPT_NOT_FOUND("E302", "부서를 찾을 수 없습니다."),

    /** 잘못된 휴가 날짜 */
    INVALID_LEAVE_DATE("E303", "휴가 날짜가 올바르지 않습니다."),

    /** 잔여 연차 부족 */
    INSUFFICIENT_LEAVE("E304", "잔여 연차가 부족합니다."),

    /** 이미 처리된 휴가 신청 */
    LEAVE_ALREADY_PROCESSED("E305", "이미 처리된 휴가 신청입니다."),

    // =============================================
    // E4xx: 재무/회계 오류
    // =============================================

    /** 계정과목 없음 */
    ACCOUNT_NOT_FOUND("E400", "계정과목을 찾을 수 없습니다."),

    /** 전표 없음 */
    VOUCHER_NOT_FOUND("E401", "전표를 찾을 수 없습니다."),

    /** 전표 차대변 불일치 */
    VOUCHER_UNBALANCED("E402", "차변 합계와 대변 합계가 일치하지 않습니다."),

    /** 이미 승인된 전표 */
    VOUCHER_ALREADY_APPROVED("E403", "이미 승인된 전표입니다."),

    // =============================================
    // E5xx: 구매/재고 오류
    // =============================================

    /** 거래처 없음 */
    VENDOR_NOT_FOUND("E500", "거래처를 찾을 수 없습니다."),

    /** 품목 없음 */
    ITEM_NOT_FOUND("E501", "품목을 찾을 수 없습니다."),

    /** 재고 부족 */
    INSUFFICIENT_STOCK("E502", "재고가 부족합니다."),

    /** 발주 없음 */
    ORDER_NOT_FOUND("E503", "발주를 찾을 수 없습니다."),

    // =============================================
    // E6xx: 영업 오류
    // =============================================

    /** 수주 없음 */
    SALES_ORDER_NOT_FOUND("E600", "수주를 찾을 수 없습니다.");

    /** 에러 코드 (예: "E300") */
    private final String code;

    /** 에러 메시지 (한글) */
    private final String message;
}
